-- ─── 6. Role-check helpers (SECURITY DEFINER, STABLE) ──────────────────
create or replace function public.current_user_role()
returns public.user_role
language sql stable security definer set search_path = public as $$
  select role from public.profiles where id = auth.uid();
$$;

create or replace function public.is_finance()
returns boolean
language sql stable security definer set search_path = public as $$
  select coalesce((select role from public.profiles where id = auth.uid())
                  in ('finance','admin'), false);
$$;

create or replace function public.is_manager()
returns boolean
language sql stable security definer set search_path = public as $$
  select coalesce((select role from public.profiles where id = auth.uid())
                  in ('manager','finance','admin'), false);
$$;

grant execute on function public.current_user_role() to authenticated;
grant execute on function public.is_finance()        to authenticated;
grant execute on function public.is_manager()        to authenticated;

-- ─── 7. Tighten refund_approvals UPDATE policy ─────────────────────────
-- Existing policy "refundapprovals_update" from 20260604200000:255-258
-- was `using (is_internal_user())` with no WITH CHECK — meaning Postgres
-- inherits USING as the new-row check, so any internal user could flip
-- finance fields. Replace with explicit WITH CHECK requiring is_manager()
-- (which includes finance + admin). SELECT/INSERT/DELETE policies
-- unaffected.
drop policy if exists "refundapprovals_update" on public.refund_approvals;

create policy "refundapprovals_update"
  on public.refund_approvals for update
  to authenticated
  using (public.is_internal_user())
  with check (public.is_manager());
