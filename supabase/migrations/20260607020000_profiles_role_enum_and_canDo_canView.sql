-- ─── 2. user_role enum ──────────────────────────────────────────────────
create type public.user_role as enum ('operator','manager','finance','admin');

-- ─── 3. Normalize legacy values + cast column ───────────────────────────
-- Existing rows have role='member' (the old default). 'member' isn't
-- in the enum — normalize first so the cast doesn't fail.
update public.profiles
   set role = 'operator'
 where role is null or role not in ('operator','manager','finance','admin');

alter table public.profiles
  alter column role drop default;

alter table public.profiles
  alter column role type public.user_role using role::public.user_role;

alter table public.profiles
  alter column role set default 'operator',
  alter column role set not null;

-- ─── 4. handle_new_user trigger — set role explicitly ──────────────────
-- Trigger was last touched by 20260417023746_seed_team_invite_list.sql
-- when display_name was added; this re-creates it with role='operator'
-- so new sign-ins land in the right enum value (Postgres column default
-- handles this too, but being explicit is safer if defaults change).
create or replace function public.handle_new_user()
returns trigger language plpgsql security definer as $$
declare override_name text;
begin
  select display_name into override_name
    from public.team_invite_list where email = new.email;
  insert into public.profiles (id, display_name, role)
  values (
    new.id,
    coalesce(override_name, new.raw_user_meta_data->>'full_name', split_part(new.email, '@', 1)),
    'operator'
  );
  return new;
end $$;

